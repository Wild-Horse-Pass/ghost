//! Error types for Ghost Locks

use thiserror::Error;

/// Errors that can occur in Ghost Locks operations
#[derive(Debug, Error)]
pub enum GhostLockError {
    /// Invalid denomination
    #[error("Invalid denomination: {0}")]
    InvalidDenomination(String),

    /// Script building failed
    #[error("Script error: {0}")]
    ScriptError(String),

    /// Invalid key
    #[error("Invalid key: {0}")]
    InvalidKey(String),

    /// Invalid state transition
    #[error("Invalid state transition: {0}")]
    InvalidStateTransition(String),

    /// Timelock not expired
    #[error("Timelock not expired: {0} blocks remaining")]
    TimelockNotExpired(u32),

    /// Lock not found
    #[error("Lock not found: {0}")]
    LockNotFound(String),

    /// Insufficient balance
    #[error("Insufficient balance: have {have}, need {need}")]
    InsufficientBalance { have: u64, need: u64 },

    /// Invalid creation height
    #[error("Invalid creation height: {0}")]
    InvalidCreationHeight(String),

    /// Timelock too short
    #[error("Timelock too short: {blocks} blocks, minimum is {minimum}")]
    TimelockTooShort { blocks: u32, minimum: u32 },
}

impl From<bitcoin::secp256k1::Error> for GhostLockError {
    fn from(e: bitcoin::secp256k1::Error) -> Self {
        GhostLockError::InvalidKey(e.to_string())
    }
}
