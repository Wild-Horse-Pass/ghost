//! Error types for Ghost Keys

use thiserror::Error;

/// Errors that can occur in Ghost Keys operations
#[derive(Debug, Error)]
pub enum GhostKeyError {
    /// Invalid secret key bytes
    #[error("Invalid secret key: {0}")]
    InvalidSecretKey(String),

    /// Invalid public key bytes
    #[error("Invalid public key: {0}")]
    InvalidPublicKey(String),

    /// Bech32 encoding error
    #[error("Bech32 encoding error: {0}")]
    Bech32Error(String),

    /// Invalid Ghost ID format
    #[error("Invalid Ghost ID format: {0}")]
    InvalidGhostId(String),

    /// Cryptographic operation failed
    #[error("Crypto error: {0}")]
    CryptoError(String),

    /// Key derivation failed
    #[error("Key derivation failed: {0}")]
    DerivationError(String),

    /// Invalid payment metadata
    #[error("Invalid metadata: {0}")]
    InvalidMetadata(String),

    /// Tweak derivation failed (should never happen with constant-time implementation)
    #[error("Invalid tweak: {0}")]
    InvalidTweak(String),
}

impl From<secp256k1::Error> for GhostKeyError {
    fn from(e: secp256k1::Error) -> Self {
        GhostKeyError::CryptoError(e.to_string())
    }
}

impl From<bech32::primitives::hrp::Error> for GhostKeyError {
    fn from(e: bech32::primitives::hrp::Error) -> Self {
        GhostKeyError::Bech32Error(e.to_string())
    }
}
