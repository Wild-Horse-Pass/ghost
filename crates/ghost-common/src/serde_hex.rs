/// Serialize/deserialize Vec<[u8; 32]> as vec of hex strings
pub mod vec_bytes32 {
    use super::*;

    pub fn serialize<S>(bytes: &[[u8; 32]], serializer: S) -> Result<S::Ok, S::Error>
    where
        S: Serializer,
    {
        use serde::ser::SerializeSeq;
        let mut seq = serializer.serialize_seq(Some(bytes.len()))?;
        for b in bytes {
            seq.serialize_element(&hex::encode(b))?;
        }
        seq.end()
    }

    pub fn deserialize<'de, D>(deserializer: D) -> Result<Vec<[u8; 32]>, D::Error>
    where
        D: Deserializer<'de>,
    {
        let strings: Vec<String> = Vec::deserialize(deserializer)?;
        strings
            .into_iter()
            .map(|s| {
                let bytes = hex::decode(&s).map_err(serde::de::Error::custom)?;
                if bytes.len() != 32 {
                    return Err(serde::de::Error::custom(format!(
                        "expected 32 bytes, got {}",
                        bytes.len()
                    )));
                }
                let mut arr = [0u8; 32];
                arr.copy_from_slice(&bytes);
                Ok(arr)
            })
            .collect()
    }
}

#[cfg(test)]
mod tests {
    use super::*;
    use serde::{Deserialize, Serialize};

    #[derive(Serialize, Deserialize, Debug, PartialEq)]
    struct TestStruct32 {
        #[serde(with = "bytes32")]
        data: [u8; 32],
    }

    #[derive(Serialize, Deserialize, Debug, PartialEq)]
    struct TestStruct64 {
        #[serde(with = "bytes64")]
        data: [u8; 64],
    }

    #[test]
    fn test_bytes32_roundtrip() {
        let original = TestStruct32 { data: [0xab; 32] };
        let json = serde_json::to_string(&original).unwrap();
        let decoded: TestStruct32 = serde_json::from_str(&json).unwrap();
        assert_eq!(original, decoded);
    }

    #[test]
    fn test_bytes64_roundtrip() {
        let original = TestStruct64 { data: [0xcd; 64] };
        let json = serde_json::to_string(&original).unwrap();
        let decoded: TestStruct64 = serde_json::from_str(&json).unwrap();
        assert_eq!(original, decoded);
    }
}
