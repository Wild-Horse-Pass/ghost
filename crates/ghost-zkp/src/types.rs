/// Witness for a single payment's state transition
///
/// Contains all data needed to prove that a payment correctly
/// transitions state from one merkle root to another.
#[derive(Debug, Clone)]
pub struct PaymentTransitionWitness {
    /// Sender's balance before this payment
    pub sender_balance_before: u64,
    /// Recipient's balance before this payment
    pub recipient_balance_before: u64,
    /// Payment amount
    pub amount: u64,

    /// Sender's leaf index in the tree
    pub sender_index: u64,
    /// Merkle proof for sender's balance
    pub sender_merkle_proof: MerkleProof,

    /// Recipient's leaf index in the tree
    pub recipient_index: u64,
    /// Merkle proof for recipient's balance (against intermediate root)
    pub recipient_merkle_proof: MerkleProof,
}

impl PaymentTransitionWitness {
    /// Create a new payment transition witness
    pub fn new(
        sender_balance_before: u64,
        recipient_balance_before: u64,
        amount: u64,
        sender_index: u64,
        sender_merkle_proof: MerkleProof,
        recipient_index: u64,
        recipient_merkle_proof: MerkleProof,
    ) -> Self {
        Self {
            sender_balance_before,
            recipient_balance_before,
            amount,
            sender_index,
            sender_merkle_proof,
            recipient_index,
            recipient_merkle_proof,
        }
    }

    /// Sender's balance after this payment
    pub fn sender_balance_after(&self) -> u64 {
        self.sender_balance_before.saturating_sub(self.amount)
    }

    /// Recipient's balance after this payment
    pub fn recipient_balance_after(&self) -> u64 {
        self.recipient_balance_before.saturating_add(self.amount)
    }

    /// Validate that the witness is internally consistent
    pub fn validate(&self) -> Result<(), WitnessValidationError> {
        if self.sender_balance_before < self.amount {
            return Err(WitnessValidationError::InsufficientBalance {
                tx_index: 0,
                balance: self.sender_balance_before,
                amount: self.amount,
            });
        }
        Ok(())
    }
}

/// Enhanced block witness with full merkle proof data for ZK state transitions
///
/// This witness type enables fully trustless verification where
/// the state root transition is proven cryptographically.
#[derive(Debug, Clone)]
pub struct BlockWitnessV2 {
    /// Block height
    pub height: u64,
    /// Previous state root
    pub prev_state_root: [u8; 32],
    /// New state root (after all payments)
    pub new_state_root: [u8; 32],
    /// Payment transitions with merkle proofs
    pub transitions: Vec<PaymentTransitionWitness>,
    /// Merkle tree depth
    pub tree_depth: usize,
}

impl BlockWitnessV2 {
    /// Create a new block witness
    pub fn new(
        height: u64,
        prev_state_root: [u8; 32],
        new_state_root: [u8; 32],
        transitions: Vec<PaymentTransitionWitness>,
        tree_depth: usize,
    ) -> Self {
        Self {
            height,
            prev_state_root,
            new_state_root,
            transitions,
            tree_depth,
        }
    }

    /// Create an empty block witness (no transactions)
    pub fn empty(height: u64, state_root: [u8; 32], tree_depth: usize) -> Self {
        Self {
            height,
            prev_state_root: state_root,
            new_state_root: state_root,
            transitions: Vec::new(),
            tree_depth,
        }
    }

    /// Number of transactions in the witness
    pub fn tx_count(&self) -> usize {
        self.transitions.len()
    }

    /// Validate witness consistency
    pub fn validate(&self) -> Result<(), WitnessValidationError> {
        for (i, tx) in self.transitions.iter().enumerate() {
            if tx.sender_balance_before < tx.amount {
                return Err(WitnessValidationError::InsufficientBalance {
                    tx_index: i,
                    balance: tx.sender_balance_before,
                    amount: tx.amount,
                });
            }
        }
        Ok(())
    }

    /// Convert to legacy BlockWitness format (for backwards compatibility)
    pub fn to_legacy(&self) -> BlockWitness {
        let transactions = self
            .transitions
            .iter()
            .map(|t| PaymentWitness {
                sender: [0u8; 32], // Not used in legacy format
                recipient: [0u8; 32],
                amount: t.amount,
                signature: [0u8; 64],
                sender_balance_before: t.sender_balance_before,
                sender_merkle_proof: t.sender_merkle_proof.clone(),
                recipient_balance_before: t.recipient_balance_before,
                recipient_merkle_proof: t.recipient_merkle_proof.clone(),
            })
            .collect();

        BlockWitness {
            height: self.height,
            prev_state: StateSnapshot::new(self.prev_state_root, vec![]),
            transactions,
            new_state: StateSnapshot::new(self.new_state_root, vec![]),
        }
    }
}

/// Intermediate root computed during state transitions
///
/// Tracks the state root after each payment in a block.
#[derive(Debug, Clone)]
pub struct IntermediateRoot {
    /// The root value
    pub root: [u8; 32],
    /// Index of the payment that produced this root (0 = after first payment)
    pub after_payment_index: usize,
}

/// Account state for building merkle trees
#[derive(Debug, Clone, Default)]
pub struct AccountState {
    /// Account index in the tree
    pub index: u64,
    /// Current balance
    pub balance: u64,
}

