/// Size of a Groth16 proof in bytes
/// - A point (G1): 48 bytes (compressed BLS12-381)
/// - B point (G2): 96 bytes (compressed BLS12-381)
/// - C point (G1): 48 bytes (compressed BLS12-381)
///
/// Total: 192 bytes
pub const GROTH16_PROOF_SIZE: usize = 192;

/// Size of a G1 point (compressed)
pub const G1_POINT_SIZE: usize = 48;

/// Size of a G2 point (compressed)
pub const G2_POINT_SIZE: usize = 96;

/// Minimum valid proof size (must have at least the header)
pub const MIN_PROOF_SIZE: usize = GROTH16_PROOF_SIZE;

/// Proof that a block's state transition is valid
///
/// This proof is generated by the block proposer and verified by validators.
/// Once verified and the block is finalized, the proof can be discarded.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct BlockProof {
    /// Block height
    pub height: u64,
    /// Previous state root (public input)
    pub prev_state_root: [u8; 32],
    /// New state root (public input)
    pub new_state_root: [u8; 32],
    /// Number of transactions in the block
    pub tx_count: u32,
    /// The serialized proof bytes
    pub proof: Vec<u8>,
    /// Proof system version (for future upgrades)
    pub version: u8,
}

impl BlockProof {
    /// Current proof version
    pub const CURRENT_VERSION: u8 = 1;

    /// Get the public inputs for verification
    pub fn public_inputs(&self) -> Vec<[u8; 32]> {
        vec![self.prev_state_root, self.new_state_root]
    }

    /// Get proof size in bytes
    pub fn size(&self) -> usize {
        self.proof.len()
    }

    /// Create a new block proof
    pub fn new(
        height: u64,
        prev_state_root: [u8; 32],
        new_state_root: [u8; 32],
        tx_count: u32,
        proof: Vec<u8>,
    ) -> Self {
        Self {
            height,
            prev_state_root,
            new_state_root,
            tx_count,
            proof,
            version: Self::CURRENT_VERSION,
        }
    }
}

/// Witness data for proving a block (private inputs)
///
/// This contains all the data needed to generate a proof that
/// the state transition is valid.
#[derive(Debug, Clone)]
pub struct BlockWitness {
    /// Block height
    pub height: u64,
    /// State before applying transactions
    pub prev_state: StateSnapshot,
    /// Transactions to apply (with merkle proofs)
    pub transactions: Vec<PaymentWitness>,
    /// State after applying transactions
    pub new_state: StateSnapshot,
}

impl BlockWitness {
    /// Create a new block witness
    pub fn new(
        height: u64,
        prev_state: StateSnapshot,
        transactions: Vec<PaymentWitness>,
        new_state: StateSnapshot,
    ) -> Self {
        Self {
            height,
            prev_state,
            transactions,
            new_state,
        }
    }

    /// Number of transactions in the witness
    pub fn tx_count(&self) -> usize {
        self.transactions.len()
    }

    /// Validate witness consistency (balances add up, etc.)
    pub fn validate(&self) -> Result<(), WitnessValidationError> {
        // Check each payment has valid balance transition
        for (i, tx) in self.transactions.iter().enumerate() {
            if tx.sender_balance_before < tx.amount {
                return Err(WitnessValidationError::InsufficientBalance {
                    tx_index: i,
                    balance: tx.sender_balance_before,
                    amount: tx.amount,
                });
            }
        }

        Ok(())
    }
}

/// Witness validation error
#[derive(Debug, Clone)]
pub enum WitnessValidationError {
    /// Sender has insufficient balance
    InsufficientBalance {
        tx_index: usize,
        balance: u64,
        amount: u64,
    },
    /// Merkle proof is invalid
    InvalidMerkleProof { tx_index: usize },
    /// State roots don't match
    StateRootMismatch,
}

impl std::fmt::Display for WitnessValidationError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::InsufficientBalance {
                tx_index,
                balance,
                amount,
            } => {
                write!(
                    f,
                    "Transaction {} has insufficient balance: {} < {}",
                    tx_index, balance, amount
                )
            }
            Self::InvalidMerkleProof { tx_index } => {
                write!(f, "Transaction {} has invalid merkle proof", tx_index)
            }
            Self::StateRootMismatch => {
                write!(f, "State roots don't match after applying transactions")
            }
        }
    }
}

impl std::error::Error for WitnessValidationError {}

/// Witness for a single payment transaction
#[derive(Debug, Clone)]
pub struct PaymentWitness {
    /// Sender's Ghost ID (32 bytes)
    pub sender: [u8; 32],
    /// Recipient's Ghost ID (32 bytes)
    pub recipient: [u8; 32],
    /// Amount in satoshis
    pub amount: u64,
    /// Sender's Schnorr signature (64 bytes)
    pub signature: [u8; 64],
    /// Sender's balance before this payment
    pub sender_balance_before: u64,
    /// Merkle proof for sender's balance in previous state
    pub sender_merkle_proof: MerkleProof,
    /// Recipient's balance before this payment
    pub recipient_balance_before: u64,
    /// Merkle proof for recipient's balance in previous state
    pub recipient_merkle_proof: MerkleProof,
}

impl PaymentWitness {
    /// Sender's balance after this payment
    pub fn sender_balance_after(&self) -> u64 {
        self.sender_balance_before.saturating_sub(self.amount)
    }

    /// Recipient's balance after this payment
    pub fn recipient_balance_after(&self) -> u64 {
        self.recipient_balance_before.saturating_add(self.amount)
    }

    /// Create signing message for this payment
    pub fn signing_message(&self) -> [u8; 32] {
        use sha2::{Digest, Sha256};
        let mut hasher = Sha256::new();
        hasher.update(b"GhostPay/Payment/v1");
        hasher.update(self.sender);
        hasher.update(self.recipient);
        hasher.update(self.amount.to_le_bytes());
        hasher.finalize().into()
    }
}

/// Snapshot of state at a point in time
#[derive(Debug, Clone)]
pub struct StateSnapshot {
    /// Merkle root of the balance tree
    pub root: [u8; 32],
    /// Affected balances (only accounts touched by this block)
    /// Map from account ID to balance
    pub balances: Vec<([u8; 32], u64)>,
}

impl StateSnapshot {
    /// Create a new state snapshot
    pub fn new(root: [u8; 32], balances: Vec<([u8; 32], u64)>) -> Self {
        Self { root, balances }
    }

    /// Create empty state snapshot
    pub fn empty() -> Self {
        Self {
            root: [0u8; 32],
            balances: Vec::new(),
        }
    }

    /// Get balance for an account
    pub fn get_balance(&self, account: &[u8; 32]) -> Option<u64> {
        self.balances
            .iter()
            .find(|(id, _)| id == account)
            .map(|(_, balance)| *balance)
    }
}

/// Merkle inclusion proof for a leaf in the balance tree
#[derive(Debug, Clone)]
pub struct MerkleProof {
    /// Index of the leaf in the tree
    pub leaf_index: u64,
    /// Sibling hashes from leaf to root
    pub siblings: Vec<[u8; 32]>,
}

impl MerkleProof {
    /// Create a new merkle proof
    pub fn new(leaf_index: u64, siblings: Vec<[u8; 32]>) -> Self {
        Self {
            leaf_index,
            siblings,
        }
    }

    /// Depth of the merkle tree (number of siblings)
    pub fn depth(&self) -> usize {
        self.siblings.len()
    }

    /// Verify this proof against a root
    pub fn verify(&self, leaf_hash: [u8; 32], root: [u8; 32]) -> bool {
        let computed_root = self.compute_root(leaf_hash);
        computed_root == root
    }

    /// Compute the root from a leaf hash
    pub fn compute_root(&self, leaf_hash: [u8; 32]) -> [u8; 32] {
        use sha2::{Digest, Sha256};

        let mut current = leaf_hash;
        let mut index = self.leaf_index;

        for sibling in &self.siblings {
            let mut hasher = Sha256::new();

            if index.is_multiple_of(2) {
                // Current is left child
                hasher.update(current);
                hasher.update(sibling);
            } else {
                // Current is right child
                hasher.update(sibling);
                hasher.update(current);
            }

            current = hasher.finalize().into();
            index /= 2;
        }

        current
    }
}

/// Verification key for block proofs
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct VerificationKey {
    /// Serialized verification key bytes
    pub data: Vec<u8>,
    /// Version of the verification key
    pub version: u8,
}

impl VerificationKey {
    /// Create a new verification key
    pub fn new(data: Vec<u8>) -> Self {
        Self {
            data,
            version: BlockProof::CURRENT_VERSION,
        }
    }
}

/// Proving parameters for block proofs
#[derive(Debug, Clone)]
pub struct ProvingParams {
    /// Serialized proving parameters
    pub data: Vec<u8>,
    /// Maximum transactions per block this params supports
    pub max_txs: usize,
    /// Merkle tree depth this params supports
    pub tree_depth: usize,
    /// Version
    pub version: u8,
}

impl ProvingParams {
    /// Create new proving params
    pub fn new(data: Vec<u8>, max_txs: usize, tree_depth: usize) -> Self {
        Self {
            data,
            max_txs,
            tree_depth,
            version: BlockProof::CURRENT_VERSION,
        }
    }
}

