//! Ghost Accounting - Economics and Payout Calculations
//!
//! This crate handles all economic calculations for Bitcoin Ghost:
//!
//! - **Share Accounting**: Track miner shares per round
//! - **Payout Calculation**: PPLNS-style proportional payouts
//! - **Node Rewards**: 5-4-3-2-1 capability-based share system
//! - **Treasury Management**: Pool fee allocation
//! - **Coinbase Construction**: Build payout transactions
//!
//! Economic flow:
//! 1. Block subsidy + TX fees arrive
//! 2. Pool fee (1%) → Treasury
//! 3. TX fees → Node that built the block
//! 4. Remaining subsidy split: 50% miners, 50% node rewards
//! 5. Miner payouts proportional to work contributed
//! 6. Node rewards proportional to capability shares (5-4-3-2-1)

pub mod coinbase;
pub mod payout;
pub mod shares;
pub mod treasury;

pub use coinbase::*;
pub use payout::*;
pub use shares::*;
pub use treasury::*;
