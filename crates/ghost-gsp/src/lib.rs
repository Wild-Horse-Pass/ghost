//! Ghost Service Provider (GSP) - Server library for light wallet support
//!
//! GSP enables users to use Ghost Pay without running a full node by providing:
//! - REST API for registration and session management
//! - WebSocket API for real-time balance and payment notifications
//! - Proxy to ghost-pay-node for payment routing
//!
//! # Architecture
//!
//! ```text
//! в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ          в”Ңв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”җ
//! в”Ӯ     Light Wallet        в”Ӯ   WSS    в”Ӯ          GSP            в”Ӯ
//! в”Ӯ  (Keys stay local)      в”Ӯв—„в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв–әв”Ӯ  (Runs full node)       в”Ӯ
//! в”Ӯ  - Signing              в”Ӯ          в”Ӯ  - Routes payments      в”Ӯ
//! в”Ӯ  - Key management       в”Ӯ          в”Ӯ  - Balance queries      в”Ӯ
//! в”Ӯ  - Address derivation   в”Ӯ          в”Ӯ  - Settlement           в”Ӯ
//! в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ          в””в”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”Җв”ҳ
//! ```
//!
//! # Security Model
//!
//! - GSP verifies signatures but **never holds keys**
//! - Cannot spend funds or sign transactions
//! - Wallet ID is pseudonymous (derived from pubkey)
//! - Clients can switch GSPs if one becomes unavailable
//!
//! # Example
//!
//! ```no_run
//! use ghost_gsp::{GspServer, GspConfig};
//!
//! #[tokio::main]
//! async fn main() {
//!     let config = GspConfig::default();
//!     let server = GspServer::new(config).await.unwrap();
//!     server.run().await.unwrap();
//! }
//! ```

