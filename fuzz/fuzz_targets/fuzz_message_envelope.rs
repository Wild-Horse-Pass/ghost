//! Fuzz target for P2P message envelope parsing
//!
//! Tests that the message validator and deserializer never panic
//! on arbitrary input.

#![no_main]

use libfuzzer_sys::fuzz_target;
use ghost_consensus::message::MessageEnvelope;
use ghost_consensus::message_validator::{validate_envelope_header, validate_and_verify};

fuzz_target!(|data: &[u8]| {
    // Header validation should never panic
    let _ = validate_envelope_header(data);

    // Deserialization should never panic
    let _ = MessageEnvelope::deserialize(data);

    // Full validation should never panic (may error on invalid data)
    let _ = validate_and_verify(data);
});
