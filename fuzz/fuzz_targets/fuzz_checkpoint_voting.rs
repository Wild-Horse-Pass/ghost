//! Fuzz target for checkpoint vote deserialization and signature verification
//!
//! Tests that Vote deserialization and verify_vote_signature never panic
//! on arbitrary input. The Vote struct uses custom serde for [u8; 64]
//! signatures and [u8; 32] node IDs which are worth exercising.

#![no_main]

use libfuzzer_sys::fuzz_target;
use arbitrary::Arbitrary;

#[allow(deprecated)]
use ghost_consensus::voting::{
    Vote, compute_vote_signing_message, verify_vote_signature,
};

#[derive(Arbitrary, Debug)]
struct FuzzVoteInput {
    // Raw bytes for Vote JSON deserialization
    json_bytes: Vec<u8>,
    // Structured inputs for compute/verify paths
    voter: [u8; 32],
    proposal_hash: [u8; 32],
    signature: [u8; 64],
    round_id: u64,
    approve: bool,
}

fuzz_target!(|input: FuzzVoteInput| {
    // 1. JSON deserialization of Vote ‚ÄĒ must never panic
    let _ = serde_json::from_slice::<Vote>(&input.json_bytes);

    // 2. Signing message computation ‚ÄĒ must never panic
    let _msg = compute_vote_signing_message(
        input.round_id,
        &input.proposal_hash,
        &input.voter,
        input.approve,
    );

    // 3. Signature verification with arbitrary data ‚ÄĒ must never panic
    let vote = Vote::new(input.voter, input.approve, input.signature);
    #[allow(deprecated)]
    let _ = verify_vote_signature(&vote, &input.proposal_hash);
});
